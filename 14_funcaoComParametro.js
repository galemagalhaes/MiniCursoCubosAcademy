//Função com parâmetro

function cumprimentarPessoa(nome) { //'nome' é passado como parâmetro, vai funcionar como uma pseudo variável que só existe dentro da função
  console.log("Olá, " + nome ); //nessa concatenação será impresso o que for passado como argumento do parâmetro nome
}

cumprimentarPessoa("Glaucia"); //argumento "Glaucia"
cumprimentarPessoa("Alexandre"); //argumento "Alexandre"